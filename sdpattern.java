import java.util.*;
class sdpattern
{
        public static void main(String args[])
        {
            int i=1;
            Scanner sc=new Scanner(System.in);
            int n=sc.nextInt();
            while(i<=10)
            {
                System.out.println(n + " x " + i + " = " + n * i);
                i++;
            }
        }
}
